export function getLaptopHeader() {
  return laptopDeals.map(({ name, id, img }) => ({ name, id, img }))
}
export const getLaptopHeaderName = (laptopsessionid) => {
  return laptopDeals.find(({ id }) => id === laptopsessionid).name
}
export const getLaptop = (laptopsessionid) => {
  return laptopDeals.find(({ id }) => id === laptopsessionid).session
}
export const getSoleLaptopHeader = (laptopsessionid, phoneid) => {
  return laptopDeals.find(({ id }) => id === laptopsessionid).session.find(({ id }) => id === phoneid)
}
