export function getPhoneHeader() {
  return phoneDeals.map(({ name, id, img }) => ({ name, id, img }))
}
export const getPhoneHeaderName = (phonesessionid) => {
  return phoneDeals.find(({ id }) => id === phonesessionid).name
}
export const getPhone = (phonesessionid) => {
  return phoneDeals.find(({ id }) => id === phonesessionid).session
}
export const getSolePhone = (phonesessionid, phoneid) => {
  return phoneDeals.find(({ id }) => id === phonesessionid).session.find(({ id }) => id === phoneid)
}
